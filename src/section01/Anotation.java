package section01;

public class Anotation {
    public static void main(String[] args) {
        // 한줄에 하나의 실행문만 작성
        int a = 1; 
        int b = 2;

        // 한줄에 여러개의 실행문 작성
        a = 1; b = 2;

        // 하나의 실행문에 여러 줄로 작성
        a =
        1;
        b = 
        2;
        
        System.out.println(a+b);
    }
}